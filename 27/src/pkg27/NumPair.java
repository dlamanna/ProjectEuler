public class NumPair {
    public int numPrimes;
    public int a;
    public int b;
    NumPair() {
        numPrimes = 0;
        a = 0;
        b = 0;
    }
    NumPair(int _a, int _b, int _numPrimes) {
        a = _a;
        b = _b;
        numPrimes = _numPrimes;
    }
}
